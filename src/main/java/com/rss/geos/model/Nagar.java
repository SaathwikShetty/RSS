package com.rss.geos.model;

import java.util.Date;

public class Nagar {
	private int nagaraID;
	private String nagaraName;
	private Date createdDateTime;

	public int getNagaraID() {
		return nagaraID;
	}

	public void setNagaraID(int nagaraID) {
		this.nagaraID = nagaraID;
	}

	public String getNagaraName() {
		return nagaraName;
	}

	public void setNagaraName(String nagaraName) {
		this.nagaraName = nagaraName;
	}

	public Date getCreatedDateTime() {
		return createdDateTime;
	}

	public void setCreatedDateTime(Date createdDateTime) {
		this.createdDateTime = createdDateTime;
	}

}
