package com.rss.geos.model;

import java.util.Date;

public class Vasati {
	private String vasatiID;
	private String vasathiName;
	private Date createdDateTime;

	public String getVasatiID() {
		return vasatiID;
	}

	public void setVasatiID(String vasatiID) {
		this.vasatiID = vasatiID;
	}

	public String getVasathiName() {
		return vasathiName;
	}

	public void setVasathiName(String vasathiName) {
		this.vasathiName = vasathiName;
	}

	public Date getCreatedDateTime() {
		return createdDateTime;
	}

	public void setCreatedDateTime(Date createdDateTime) {
		this.createdDateTime = createdDateTime;
	}
}
