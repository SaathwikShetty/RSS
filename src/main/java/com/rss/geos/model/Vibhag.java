package com.rss.geos.model;

import java.util.Date;

public class Vibhag {
	private int vibhagaID;
	private String vibhagaName;
	private Date createdDateTime;

	public int getVibhagaID() {
		return vibhagaID;
	}

	public void setVibhagaID(int vibhagaID) {
		this.vibhagaID = vibhagaID;
	}

	public String getVibhagaName() {
		return vibhagaName;
	}

	public void setVibhagaName(String vibhagaName) {
		this.vibhagaName = vibhagaName;
	}

	public Date getCreatedDateTime() {
		return createdDateTime;
	}

	public void setCreatedDateTime(Date createdDateTime) {
		this.createdDateTime = createdDateTime;
	}

}
