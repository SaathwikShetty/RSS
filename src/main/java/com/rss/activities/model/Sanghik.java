package com.rss.activities.model;

public class Sanghik {

	private String sanghikID;
	private String startTime;
	private String endTime;
	private String sanghikLocation;

	public String getSanghikID() {
		return sanghikID;
	}

	public void setSanghikID(String sanghikID) {
		this.sanghikID = sanghikID;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getSanghikLocation() {
		return sanghikLocation;
	}

	public void setSanghikLocation(String sanghikLocation) {
		this.sanghikLocation = sanghikLocation;
	}
	
}
